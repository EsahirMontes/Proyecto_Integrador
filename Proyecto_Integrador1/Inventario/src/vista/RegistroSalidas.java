    private void btnBuscarSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarSActionPerformed
        // if agregado
        if(txtIdSa.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Ingrese el ID de la salida");
            limpiarCamposSa();
            return;
        }
        //
        sa.setId_salida(Integer.parseInt(txtIdSa.getText()));
        if(daoSa.buscar(sa)){
            txtIdSa.setText(sa.getId_salida()+"");
            txtIdUs.setText(sa.getId_usuario()+"");
            txtIdP.setText(sa.getId_producto()+"");
            txtCan.setText(sa.getCantidad()+"");
            txtFe.setText(sa.getFecha_salida()+"");
            cboM.setSelectedItem(sa.getMotivo_salida());
            
        }else{
            JOptionPane.showMessageDialog(null, "La salida no existe");
            limpiarCamposSa();
        }
    }//GEN-LAST:event_btnBuscarSActionPerformed

    private void btnEliminarSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarSActionPerformed
        if(!txtIdSa.getText().isEmpty()){
            int confirmacion = JOptionPane.showConfirmDialog(null, "Estas seguro de eliminar la salida","Confirmar",2);
            if(confirmacion == 0){
                sa.setId_salida(Integer.parseInt(txtIdSa.getText()));
                daoSa.eliminar(sa);
                limpiarCamposSa();
                limpiarTablaSalidas();
                listarSalidas();
                JOptionPane.showMessageDialog(null, "Se elimino la salida");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Seleccione el ID de la salida");
        }
    }//GEN-LAST:event_btnEliminarSActionPerformed

    private void btnGuardarSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarSActionPerformed
        // Validar que los campos no estén vacíos
        if (txtIdUs.getText().isEmpty() ||txtIdP.getText().isEmpty()||txtCan.getText().isEmpty() || txtFe.getText().isEmpty()||cboM.getSelectedItem().toString().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor complete todos los campos.");
            return;
        }

        // Validar que el ID de sea un numero
        
        int idUs;
        int idPro;
       
        try {
                       
            idUs = Integer.parseInt(txtIdUs.getText());            
            idPro = Integer.parseInt(txtIdP.getText());            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "El ID de usuario y producto debe ser un número.");
            return;
        }
        //validar la fecha
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDate FechaS;
        try {            
            FechaS = LocalDate.parse(txtFe.getText(),formatter);
        } catch (DateTimeParseException e) {
            JOptionPane.showMessageDialog(null, "La fecha debe estar en formato dd/MM/yyyy.");
            return;
        }
        //
               
        sa.setId_usuario(Integer.parseInt(txtIdUs.getText()));
        sa.setId_producto(Integer.parseInt(txtIdP.getText()));
        //sa.setTipo_unidad(txtTipo.getText());
        sa.setCantidad(Integer.parseInt(txtCan.getText()));
        //sa.setCosto_unit(Double.parseDouble(txtCo.getText()));
        sa.setFecha_salida(LocalDate.parse(txtFe.getText(),formatter));
        sa.setMotivo_salida(cboM.getSelectedItem().toString());
        
        // Consulta para obtener tipo_unidad y costo_unit desde la tabla entradas
        String selectEntradaSQL = "SELECT tipo_unidad, costo_unit FROM entradas WHERE id_producto = ? ORDER BY id_entrada ASC LIMIT 1";

        try (Connection con = cn.conectar();
         PreparedStatement ps = con.prepareStatement(selectEntradaSQL)) {

        ps.setInt(1, sa.getId_producto());
        ResultSet rs = ps.executeQuery();

        if (rs.next()) {
            String tipoUnidad = rs.getString("tipo_unidad");
            double costoUnit = rs.getDouble("costo_unit");

            sa.setTipo_unidad(tipoUnidad);
            sa.setCosto_unit(costoUnit);

            // Opcional: mostrar los valores en los campos para que el usuario los vea
            /*txtTipo.setText(tipoUnidad);
            txtTipo.setEditable(false); // evitar que el usuario lo modifique
            txtCo.setText(String.valueOf(costoUnit));
            txtCo.setEditable(false);*/

        } else {
            JOptionPane.showMessageDialog(null, "No se encontró información en 'entradas' para el producto con ID: " + sa.getId_producto());
            return; // detener ejecución si no hay datos para completar
        }

        // Llamar al método insert ya con todos los datos completos
        if (daoSa.insert(sa)) {
            if (!txtIdSa.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "No es necesario poner el ID de salida, el sistema lo asigna automáticamente");
            }
            JOptionPane.showMessageDialog(null, "Salida Registrada");
        } else {
            JOptionPane.showMessageDialog(null, "No se pudo registrar la Salida");
        }

        } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error al completar datos desde 'entradas': " + e.getMessage());
        }
        limpiarCamposSa();
        limpiarTablaSalidas();
        listarSalidas();
    }//GEN-LAST:event_btnGuardarSActionPerformed

