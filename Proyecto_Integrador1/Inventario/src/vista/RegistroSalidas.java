    private void listarSalidas(){
        List<Salidas> listaSa = daoSa.Listar();
        modeloSalida = (DefaultTableModel) tblSalida.getModel();
        Object[] ob = new Object[8];
        for(int i=0; i< listaSa.size(); i++){
            ob[0] = listaSa.get(i).getId_salida();
            ob[1] = listaSa.get(i).getId_usuario();
            ob[2] = listaSa.get(i).getId_producto();
            ob[3] = listaSa.get(i).getTipo_unidad();
            ob[4] = listaSa.get(i).getCantidad();
            ob[5] = listaSa.get(i).getCosto_unit();
            ob[6] = listaSa.get(i).getFecha_salida();
            ob[7] = listaSa.get(i).getMotivo_salida();
            
            
            modeloSalida.addRow(ob);
        }
        tblSalida.setModel(modeloSalida);
    }
