    private void btnEditarSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarSActionPerformed
        int fila = tblSalida.getSelectedRow();
        //validar la fecha
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        
        if(fila==-1){
            JOptionPane.showMessageDialog(null, "Seleccione un producto");
        }else{
            sa.setId_salida(Integer.parseInt(txtIdSa.getText()));
            sa.setId_usuario(Integer.parseInt(txtIdUs.getText()));
            sa.setId_producto(Integer.parseInt(txtIdP.getText()));
            sa.setCantidad(Integer.parseInt(txtCan.getText()));
            sa.setFecha_salida(LocalDate.parse(txtFe.getText(),formatter));
            sa.setMotivo_salida(cboM.getSelectedItem().toString());
            
            if(daoSa.editar(sa)){
                JOptionPane.showMessageDialog(null, "Se modifico con exito");
                limpiarCamposSa();
                limpiarTablaSalidas();
                listarSalidas();
            }
        }
    }//GEN-LAST:event_btnEditarSActionPerformed

    void limpiarCamposSa(){
        txtIdSa.setText("");
        txtIdUs.setText("");
        txtIdP.setText("");
        
        txtCan.setText("");
        
        txtFe.setText("");
        cboM.setSelectedItem("");
        
    }
    void limpiarTablaSalidas(){
        for(int i=0; i<modeloSalida.getRowCount();i++){
            modeloSalida.removeRow(i);
            i = 0-1;
        }
    }
