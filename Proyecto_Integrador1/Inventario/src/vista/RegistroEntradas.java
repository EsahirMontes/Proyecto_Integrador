    private void listarEntradas(){
        List<Entradas> listaEn = daoEn.Listar();
        modeloEntrada = (DefaultTableModel) tblEntradas.getModel();
        Object[] ob = new Object[9];
        for(int i=0; i< listaEn.size(); i++){
            ob[0] = listaEn.get(i).getId_entrada();
            ob[1] = listaEn.get(i).getId_compra();
            ob[2] = listaEn.get(i).getId_usuario();
            ob[3] = listaEn.get(i).getId_producto();
            ob[4] = listaEn.get(i).getTipo_unidad();
            ob[5] = listaEn.get(i).getCantidad();
            ob[6] = listaEn.get(i).getCosto_unit();
            ob[7] = listaEn.get(i).getFecha_vencimiento();
            ob[8] = listaEn.get(i).getFecha_entrada();
            
            
            modeloEntrada.addRow(ob);
        }
        tblEntradas.setModel(modeloEntrada);
    }
