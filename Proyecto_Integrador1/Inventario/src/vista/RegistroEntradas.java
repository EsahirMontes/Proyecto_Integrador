    private void btnBuscarEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarEActionPerformed
        // if agregado
        if(txtIdEn.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Ingrese el ID de la entrada");
            limpiarCamposEn();
            return;
        }
        //
        en.setId_entrada(Integer.parseInt(txtIdEn.getText()));
        if(daoEn.buscar(en)){
            txtIdEn.setText(en.getId_entrada()+"");
            txtIdCom.setText(en.getId_compra()+"");
            txtIdUs.setText(en.getId_usuario()+"");
            txtIdPro.setText(en.getId_producto()+"");
            txtTipo.setText(en.getTipo_unidad());
            txtCan.setText(en.getCantidad()+"");
            txtCos.setText(en.getCosto_unit()+"");
            txtFe.setText(en.getFecha_vencimiento()+"");
            txtFeE.setText(en.getFecha_entrada()+"");
            
        }else{
            JOptionPane.showMessageDialog(null, "La entrada no existe");
            limpiarCamposEn();
        }
    }//GEN-LAST:event_btnBuscarEActionPerformed

    private void btnEliminarEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarEActionPerformed
        if(!txtIdEn.getText().isEmpty()){
            int confirmacion = JOptionPane.showConfirmDialog(null, "Estas seguro de eliminar la entrada","Confirmar",2);
            if(confirmacion == 0){
                en.setId_entrada(Integer.parseInt(txtIdEn.getText()));
                daoEn.eliminar(en);
                limpiarCamposEn();
                limpiarTablaEntrada();
                listarEntradas();
                JOptionPane.showMessageDialog(null, "Se elimino la entrada");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Seleccione el ID de entrada");
        }
    }//GEN-LAST:event_btnEliminarEActionPerformed

    private void btnGuardarEnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarEnActionPerformed
        // Validar que los campos no estén vacíos
        if (txtIdCom.getText().isEmpty() ||txtIdUs.getText().isEmpty() ||txtIdPro.getText().isEmpty() ||txtTipo.getText().isEmpty() ||txtCan.getText().isEmpty() ||txtCos.getText().isEmpty() || txtFe.getText().isEmpty()|| txtFeE.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor complete todos los campos.");
            return;
        }

        // Validar que el ID de sea un numero
        int idCom;
        int idUs;
        int idPro;
       
        try {
            idCom = Integer.parseInt(txtIdCom.getText());            
            idUs = Integer.parseInt(txtIdUs.getText());            
            idPro = Integer.parseInt(txtIdPro.getText());            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "El ID de compra, usuario y producto debe ser un número.");
            return;
        }
        //validar la fecha
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        LocalDate FechaV;
        LocalDate FechaE;
        try {            
            FechaV = LocalDate.parse(txtFe.getText(),formatter);
            FechaE = LocalDate.parse(txtFeE.getText(),formatter);
        } catch (DateTimeParseException e) {
            JOptionPane.showMessageDialog(null, "La fecha debe estar en formato yyyy-MM-dd.");
            return;
        }
        //
        
        en.setId_compra(Integer.parseInt(txtIdCom.getText()));
        en.setId_usuario(Integer.parseInt(txtIdUs.getText()));
        en.setId_producto(Integer.parseInt(txtIdPro.getText()));
        en.setTipo_unidad(txtTipo.getText());
        en.setCantidad(Integer.parseInt(txtCan.getText()));
        en.setCosto_unit(Double.parseDouble(txtCos.getText()));
        en.setFecha_vencimiento(LocalDate.parse(txtFe.getText(),formatter));
        en.setFecha_entrada(LocalDate.parse(txtFeE.getText(),formatter));
        
        
        if(daoEn.insert(en)){
            //informar que el Id compra es autoasignado por el sistema
            if(!txtIdEn.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "No es necesario poner el ID de entrada, el sistema lo asigna automáticamente");
            }
            JOptionPane.showMessageDialog(null, "Entrada Registrada");
        }else {
            JOptionPane.showMessageDialog(null, "No se pudo registrar la entrada");
        }
        limpiarCamposEn();
        limpiarTablaEntrada();
        listarEntradas();
    
    }//GEN-LAST:event_btnGuardarEnActionPerformed

