    private void btnGuardarC1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarC1ActionPerformed
        // Validar que los campos no est√©n vac√≠os
        if (txtNomP.getText().isEmpty() || txtMarcaP.getText().isEmpty() || txtDesP.getText().isEmpty() || txtIdC.getText().isEmpty() || txtStockM.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor complete todos los campos.");
            return;
        }

        // Validar que el ID de categor√≠a y el stock sea un n√∫mero
        int idCategoria;
        int stockM;
        try {
            idCategoria = Integer.parseInt(txtIdC.getText());
            stockM = Integer.parseInt(txtStockM.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "El ID de categor√≠a y el stock minimo debe ser un n√∫mero.");
            return;
        }
        //
        pr.setNombre_producto(txtNomP.getText());
        pr.setMarca(txtMarcaP.getText());
        pr.setDescripcion(txtDesP.getText());
        pr.setId_categoria(Integer.parseInt(txtIdC.getText()));
        pr.setStock_minimo(Integer.parseInt(txtStockM.getText()));
        
        if(daoP.insert(pr)){
            //informar que el Id producto es autoasignado por el sistema
            if(!txtIdP.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "No es necesario poner el ID del producto, el sistema lo asigna autom√°ticamente");
            }
            JOptionPane.showMessageDialog(null, "Producto Registrado");
        }else {
            JOptionPane.showMessageDialog(null, "No se pudo registrar el producto");
        }
        limpiarCamposPro();
    }//GEN-LAST:event_btnGuardarC1ActionPerformed

