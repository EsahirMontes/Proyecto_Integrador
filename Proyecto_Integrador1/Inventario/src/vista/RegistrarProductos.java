    void limpiarTablaCategorias(){
        for(int i=0; i<modeloCategoria.getRowCount();i++){
            modeloCategoria.removeRow(i);
            i = 0-1;
        }
    }
    void limpiarTablaProductos(){
        for(int i=0; i<modeloProductos.getRowCount();i++){
            modeloProductos.removeRow(i);
            i = 0-1;
        }
    }

