    private void btnGuardarCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarCActionPerformed
        //verificar el llenado
        if (txtNomCat.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor llene el nombre de la categoria");
            return;
        }
        
        ct.setNombre_categoria(txtNomCat.getText());
        if(daoC.insert(ct)){
            if(!txtIdCat.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "No es necesario poner el ID de la categoria, el sistema lo asigna autom√°ticamente");
            }
            JOptionPane.showMessageDialog(null, "Categoria Registrada");
        }else {
            JOptionPane.showMessageDialog(null, "No se pudo registrar la categoria");
        }
        limpiarCamposCat();
        limpiarTablaCategorias();
        listarCategorias();
    }//GEN-LAST:event_btnGuardarCActionPerformed

    private void btnGuardarC1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarC1ActionPerformed
        // Validar que los campos no est√©n vac√≠os
        if (txtNomP.getText().isEmpty() || txtMarcaP.getText().isEmpty() || txtDesP.getText().isEmpty() || txtIdC.getText().isEmpty() || txtStockM.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor complete todos los campos.");
            return;
        }

        // Validar que el ID de categor√≠a y el stock sea un n√∫mero
        int idCategoria;
        int stockM;
        try {
            idCategoria = Integer.parseInt(txtIdC.getText());
            stockM = Integer.parseInt(txtStockM.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "El ID de categor√≠a y el stock minimo debe ser un n√∫mero.");
            return;
        }
        //
        pr.setNombre_producto(txtNomP.getText());
        pr.setMarca(txtMarcaP.getText());
        pr.setDescripcion(txtDesP.getText());
        pr.setId_categoria(Integer.parseInt(txtIdC.getText()));
        pr.setStock_minimo(Integer.parseInt(txtStockM.getText()));
        
        if(daoP.insert(pr)){
            //informar que el Id producto es autoasignado por el sistema
            if(!txtIdP.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "No es necesario poner el ID del producto, el sistema lo asigna autom√°ticamente");
            }
            JOptionPane.showMessageDialog(null, "Producto Registrado");
        }else {
            JOptionPane.showMessageDialog(null, "No se pudo registrar el producto");
        }
        limpiarCamposPro();
        limpiarTablaProductos();
        listarProductos();
    }//GEN-LAST:event_btnGuardarC1ActionPerformed

    private void txtEditarCatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtEditarCatActionPerformed
        int fila = tblCategorias.getSelectedRow();
        if(fila==-1){
            JOptionPane.showMessageDialog(null, "Seleccione una categoria");
        }else{
            ct.setId_categoria(Integer.parseInt(txtIdCat.getText()));
            ct.setNombre_categoria(txtNomCat.getText());
            if(daoC.editar(ct)){
                JOptionPane.showMessageDialog(null, "Se modifico con exito");
                limpiarCamposCat();
                limpiarTablaCategorias();
                listarCategorias();
            }
        }
    }//GEN-LAST:event_txtEditarCatActionPerformed

    private void btnBuscarCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarCActionPerformed
        ct.setId_categoria(Integer.parseInt(txtIdCat.getText()));
        if(daoC.buscar(ct)){
            txtIdCat.setText(ct.getId_categoria()+"");
            txtNomCat.setText(ct.getNombre_categoria());
        }else{
            JOptionPane.showMessageDialog(null, "La categoria no existe");
            limpiarCamposCat();
        }
    }//GEN-LAST:event_btnBuscarCActionPerformed

    private void btnEliminarCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarCActionPerformed
        if(!txtIdCat.getText().isEmpty()){
            int confirmacion = JOptionPane.showConfirmDialog(null, "Estas seguro de eliminar la Categoria","Confirmar",2);
            if(confirmacion == 0){
                ct.setId_categoria(Integer.parseInt(txtIdCat.getText()));
                daoC.eliminar(ct);
                limpiarCamposCat();
                limpiarTablaCategorias();
                listarCategorias();
                JOptionPane.showMessageDialog(null, "Se elimino la categoria");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Seleccione una categoria");
        }
    }//GEN-LAST:event_btnEliminarCActionPerformed

